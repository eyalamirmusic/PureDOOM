void updateWipe()
{
    if (screenWipe(WipeType::Melt, 0, 0, SCREENWIDTH, SCREENHEIGHT, 1))
        gameFlow().is_wiping_screen = false;
}

void doomLoop()
{
#if 0 // [pd] Moved to doomMain()
    if (demorecording)
        beginRecording();

    if (checkParm("-debugfile"))
    {
        Array<char, 20> filename;
        //doom_sprintf(filename, "debug%i.txt", consoleplayer);
        doom_print("debug output to: %s\n", filename.data());
        debugfile = doom_open(filename.data(), "w");
    }

    initGraphics();
#endif

    auto& state = playerState();

    // while (1)
    {
        // frame syncronous IO operations
        startFrame();

        // process one or more tics
        if (engineParams().singletics)
        {
            auto& net = netState();
            auto& clock = gameClock();

            startTic();
            processEvents();
            buildTiccmd(net.netcmds[state.consoleplayer][net.maketic % BACKUPTICS]);
            if (attractMode().advancedemo)
                doAdvanceDemo();
            menuTicker();
            gameTicker();
            clock.gametic++;
            net.maketic++;
        }
        else
        {
            tryRunTics(); // will run at least one tic
        }

        // move positional sounds
        updateSounds(state.players[state.consoleplayer].mo);

        // Update display, next frame, with current state.
        displayFrame();

#if 0 // [pd] Sound is queried by the application's audio thread.
        // Sound mixing for the buffer is snychronous.
        updateSound();

        // Synchronous sound output is explicitly called.
        // Update sound output.
        submitSound();
#endif
    }
}

