void addWadFile(std::string_view file)
{
    wadfiles.push_back(std::string {file});
}

// Joins the WAD directory and a leading-slash basename into one owned string.
// The point of it is that the size comes from the two strings themselves rather
// than from a number written beside them - see the note at the call sites, where
// one of seven such numbers was wrong and overflowed the buffer by a byte.
static std::string joinWadPath(std::string_view directory, std::string_view file)
{
    return concat(directory, file);
}

//
// IdentifyVersion
// Checks availability of IWAD files by name,
// to determine whether registered/commercial features
// should be executed (notably loading PWAD's).
//
void IdentifyVersion()
{
    auto& version = gameVersion();
    auto& opts = launchOptions();
    auto& paths = configPaths();

    auto doomwaddir = host().getenv("DOOMWADDIR").value_or(".");

    // Each of these was sized by hand, as `strlen(dir) + 1 + <basename> + 1`
    // with the basename's length written out as a literal - and one of the seven
    // had it wrong. "doomu.wad" is nine characters and was counted as eight, so
    // building the retail path wrote its terminating NUL one byte past the
    // allocation, every single boot.
    //
    // It went unseen because it depends entirely on what the allocator puts
    // there: on macOS the block's own padding absorbed it, while on Windows
    // arm64 it corrupted the following block and demo1 and demo3 died with a
    // segfault before rendering a frame. AddressSanitizer names it in one line.
    // Nothing above the allocator could see it - the goldens hash the world and
    // the picture, and both were correct right up until the heap gave out.
    //
    // So the literal is gone rather than corrected: joinWadPath measures the
    // string it is about to write, which is the only count that cannot disagree
    // with it.
    const auto doom2wad = joinWadPath(doomwaddir, "/doom2.wad"); // Commercial.
    const auto doomuwad = joinWadPath(doomwaddir, "/doomu.wad"); // Retail.
    const auto doomwad = joinWadPath(doomwaddir, "/doom.wad"); // Registered.
    const auto doom1wad = joinWadPath(doomwaddir, "/doom1.wad"); // Shareware.
    const auto plutoniawad = joinWadPath(doomwaddir, "/plutonia.wad");
    const auto tntwad = joinWadPath(doomwaddir, "/tnt.wad");
    const auto doom2fwad = joinWadPath(doomwaddir, "/doom2f.wad"); // French.

#if !defined(DOOM_WIN32)
    auto home = host().getenv("HOME");
    if (!home)
        fatalError("Error: Please set $HOME to your home directory");
#else
    auto home = std::optional<std::string> {"."};
#endif
    //doom_sprintf(basedefault, "%s/.doomrc", home);
    paths.basedefault = concat(*home, "/.doomrc");

    if (checkParm("-shdev"))
    {
        version.gamemode = GameMode::Shareware;
        opts.devparm = true;
        addWadFile(DEVDATA "doom1.wad");
        addWadFile(DEVMAPS "data_se/texture1.lmp");
        addWadFile(DEVMAPS "data_se/pnames.lmp");
        paths.basedefault = DEVDATA "default.cfg";
        return;
    }

    if (checkParm("-regdev"))
    {
        version.gamemode = GameMode::Registered;
        opts.devparm = true;
        addWadFile(DEVDATA "doom.wad");
        addWadFile(DEVMAPS "data_se/texture1.lmp");
        addWadFile(DEVMAPS "data_se/texture2.lmp");
        addWadFile(DEVMAPS "data_se/pnames.lmp");
        paths.basedefault = DEVDATA "default.cfg";
        return;
    }

    if (checkParm("-comdev"))
    {
        version.gamemode = GameMode::Commercial;
        opts.devparm = true;
        /* I don't bother
        if(plutonia)
            addWadFile (DEVDATA"plutonia.wad");
        else if(tnt)
            addWadFile (DEVDATA"tnt.wad");
        else*/
        addWadFile(DEVDATA "doom2.wad");

        addWadFile(DEVMAPS "cdata/texture1.lmp");
        addWadFile(DEVMAPS "cdata/pnames.lmp");
        paths.basedefault = DEVDATA "default.cfg";
        return;
    }

    void* f;
    if ((f = host().open(doom2fwad.c_str(), "rb")))
    {
        host().close(f);
        version.gamemode = GameMode::Commercial;
        // C'est ridicule!
        // Let's handle languages in config files, okay?
        version.language = Language::French;
        print("French version\n");
        addWadFile(doom2fwad);
        return;
    }

    if ((f = host().open(doom2wad.c_str(), "rb")))
    {
        host().close(f);
        version.gamemode = GameMode::Commercial;
        addWadFile(doom2wad);
        return;
    }

    if ((f = host().open(plutoniawad.c_str(), "rb")))
    {
        host().close(f);
        version.gamemode = GameMode::Commercial;
        addWadFile(plutoniawad);
        return;
    }

    if ((f = host().open(tntwad.c_str(), "rb")))
    {
        host().close(f);
        version.gamemode = GameMode::Commercial;
        addWadFile(tntwad);
        return;
    }

    if ((f = host().open(doomuwad.c_str(), "rb")))
    {
        host().close(f);
        version.gamemode = GameMode::Retail;
        addWadFile(doomuwad);
        return;
    }

    if ((f = host().open(doomwad.c_str(), "rb")))
    {
        host().close(f);
        version.gamemode = GameMode::Registered;
        addWadFile(doomwad);
        return;
    }

    if ((f = host().open(doom1wad.c_str(), "rb")))
    {
        host().close(f);
        version.gamemode = GameMode::Shareware;
        addWadFile(doom1wad);
        return;
    }

    print("Game mode indeterminate.\n");
    version.gamemode = GameMode::Indetermined;
}

//
// Find a Response File
//
void findResponseFile()
{
    for (int i = 1; i < myargCount(); i++)
    {
        if (myargv()[i][0] != '@')
            continue;

        auto responseFile = myargv()[i].substr(1);

        // READ THE RESPONSE FILE INTO MEMORY
        auto* handle = host().open(responseFile, "rb");

        if (!handle)
        {
            print("\nNo such response file!");
            host().exit(1);
        }

        print("Found response file ", responseFile, "!\n");

        host().seek(handle, 0, SeekOrigin::End);
        auto size = host().tell(handle);
        host().seek(handle, 0, SeekOrigin::Set);

        auto contents = std::string(static_cast<std::size_t>(size), '\0');
        host().read(handle, contents.data(), size);
        host().close(handle);

        // KEEP ALL CMDLINE ARGS FOLLOWING @RESPONSEFILE ARG
        auto moreargs =
            std::vector<std::string>(myargv().begin() + i + 1, myargv().end());

        auto rebuilt = std::vector<std::string> {myargv()[0]}; // KEEP ARGV[0]

        // Tokenised on vanilla's own character class: a token runs while the byte
        // is above ' ' and not past 'z', and the run of anything else between
        // tokens is skipped. An empty leading token is kept, as it was.
        auto k = 0;

        while (k < size)
        {
            auto start = k;

            while (k < size && contents[k] >= ' ' + 1 && contents[k] <= 'z')
                k++;

            rebuilt.emplace_back(contents, start, k - start);

            while (k < size && (contents[k] <= ' ' || contents[k] > 'z'))
                k++;
        }

        rebuilt.insert(rebuilt.end(), moreargs.begin(), moreargs.end());
        myargv() = std::move(rebuilt);

        // DISPLAY ARGS
        print(myargCount(), " command-line args:\n");

        for (auto arg = 1; arg < myargCount(); arg++)
            print(myargv()[arg], "\n");

        break;
    }
}

